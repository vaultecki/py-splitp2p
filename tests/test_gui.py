@pytest.fixture
def root():
    r = tk.Tk()
    r.withdraw()
    yield r
    r.destroy()


USERS = [
    {"public_key": "alice", "name": "Alice"},
    {"public_key": "bob", "name": "Bob"},
]


def _current_dialog(root) -> ExpenseDialog:
    tops = [w for w in root.winfo_children() if isinstance(w, tk.Toplevel)]
    assert tops, "ExpenseDialog window not found under root"
    return tops[-1]  # type: ignore[return-value]


def _open_and_save(root, act) -> dict:
    """
    Opens an ExpenseDialog, runs `act(dlg)` to fill it in and save, then
    returns dlg.result. ExpenseDialog.__init__ blocks in wait_window(), so
    `act` is scheduled via root.after() and runs once the dialog exists.
    """
    holder: dict = {}

    def run_act():
        dlg = _current_dialog(root)
        act(dlg)
        holder["result"] = dlg.result

    root.after(50, run_act)
    ExpenseDialog(root, USERS, "alice", "EUR", {})
    return holder["result"]


