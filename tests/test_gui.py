def test_build_splits_dispatches_by_mode(root):
    """App._build_splits must route to the split fn matching split_mode
    (regression for the bug where it always used split_equally)."""
    from gui import App

    app = App.__new__(App)  # bypass __init__ (no DB/network needed for this)

    equal = app._build_splits(
        "exp1",
        100,
        "alice",
        ["alice", "bob"],
        {"split_mode": "equal"},
    )
    assert sorted(s.amount for s in equal) == [50, 50]

    percent = app._build_splits(
        "exp1",
        100,
        "alice",
        ["alice", "bob"],
        {"split_mode": "percent", "split_percentages": {"alice": 70.0, "bob": 30.0}},
    )
    assert {s.debtor_key: s.amount for s in percent} == {"alice": 70, "bob": 30}

    custom = app._build_splits(
        "exp1",
        100,
        "alice",
        ["alice", "bob"],
        {"split_mode": "custom", "split_custom_amounts": {"alice": 10, "bob": 90}},
    )
    assert {s.debtor_key: s.amount for s in custom} == {"alice": 10, "bob": 90}


