def test_group_qr_encode_decode_roundtrip():
    """Regression: decode used to check for v1/pw/salt while encode produces
    v3/key/topic, so a QR generated by this app could never be scanned by it."""
    group_key = b"0" * 32
    payload = _encode_group_qr("Islandtrip", group_key, "topic-uuid", "EUR")
    result = _decode_group_qr(payload)
    assert result["name"] == "Islandtrip"
    assert result["key"] == group_key.hex()
    assert result["topic"] == "topic-uuid"
    assert result["currency"] == "EUR"


def test_group_qr_decode_rejects_unknown_version():
    with pytest.raises(ValueError):
        _decode_group_qr('{"v": 1, "name": "x", "pw": "y", "salt": "00", "currency": "EUR"}')


